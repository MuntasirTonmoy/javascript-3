// for while loop we need
//* loop variable 
//* condition
//* work that is need to do again and again means statement
//* increment or decrement means we have to change the variable value.

// show number 1-50

var num1 = 1;
while (num1 <= 50) {
    console.log(num1);
    num1++;
}

// show odd number from 1-50
var odd = 1;
while (odd <= 50) {
    console.log(odd);
    odd += 2;
}

// show even number from 1-50

var even = 2;
while (even <= 50) {
    console.log(even);
    even += 2;
}
